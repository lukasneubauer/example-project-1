    /**
     * @throws MappingException
     * @throws RuntimeException
     */
    public function testOk(): void
    {
        $client = self::createClient();

        $dic = static::getContainer();

        /** @var SessionRepository $sessionRepository */
        $sessionRepository = $dic->get(SessionRepository::class);

        $apiClientId = '1zlfxxa0381wdeogh8amw11xrudalf8rv78wo4tj';
        $apiToken = 'pxlph3gpe4jaaz87zfmgwq4bhiqcj2xofgrxqndtfssvtjap5od2rxuhivm2htpzg548cff9j6wdr2es';

        $sessionBeforeLogout = $sessionRepository->getByApiToken($apiToken);
        $this->assertInstanceOf(Session::class, $sessionBeforeLogout);
        $this->assertSame($apiClientId, $sessionBeforeLogout->getApiClientId());
        $this->assertSame($apiToken, $sessionBeforeLogout->getCurrentApiToken());

        try {
            $client->request(
                self::METHOD,
                self::ENDPOINT,
                [],
                [],
                [
                    'HTTP_Api-Key' => $dic->getParameter('api_key'),
                    'HTTP_Api-Client-Id' => $apiClientId,
                    'HTTP_Api-Token' => $apiToken,
                ]
            );

            /** @var Response $response */
            $response = $client->getResponse();
            ResponseTester::assertResponse(200, $response);
            $this->assertSame('text/plain; charset=UTF-8', $response->headers->get('Content-Type'));
            $this->assertSame('', $response->getContent());

            EntityManagerCleanup::cleanupEntityManager($dic);

            $sessionAfterLogout = $sessionRepository->getByApiToken($apiToken);
            $this->assertNull($sessionAfterLogout);
        } catch (RuntimeException $e) {
            throw $e;
        } finally {
            Database::resetDatabase($dic);
        }
    }
